@dataclass
class FingerPose:
    """Rotation angles in radians applied at the 3 finger joints.

    Convention: positive ``mcp`` flexes the finger toward the palm; positive
    ``pip``/``dip`` continue the curl.
    """
    mcp: float = 0.0
    pip: float = 0.0
    dip: float = 0.0
    abduct: float = 0.0


@dataclass
class ThumbPose:
    cmc_flex: float = 0.0
    cmc_abduct: float = 0.0
    mcp: float = 0.0
    ip: float = 0.0


@dataclass
class HandPose:
    name: str
    description: str
    fingers: dict[str, FingerPose]
    thumb: ThumbPose
    wrist_tilt_x: float = 0.0
    wrist_tilt_y: float = 0.0
    wrist_tilt_z: float = 0.0
    grasp_role: str = "ulnar"  # ulnar = pinky→thumb axis; tangential = z


def _curl(amount: float, abduct: float = 0.0) -> FingerPose:
    return FingerPose(
        mcp=amount * np.deg2rad(85.0),
        pip=amount * np.deg2rad(95.0),
        dip=amount * np.deg2rad(60.0),
        abduct=abduct,
    )


def _all_curl(amount: float, abduct: float = 0.0) -> dict[str, FingerPose]:
    return {n: _curl(amount, abduct) for n in FINGER_LENGTHS}


