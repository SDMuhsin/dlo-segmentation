#!/usr/bin/env python3
"""Procedurally generate skin-toned hand point clouds for the Phase 8 dataset.

We build hands as a union of trimesh primitives — palm box, 4 fingers as
cylinder chains, opposed thumb — pose them by joint-bending the cylinder
chains, sample 8000 surface points via :func:`trimesh.sample.sample_surface`,
and write them as ``data/objects/hand_*.npz`` matching the schema used by
:func:`texture_mapping.load_object_library`.

Phase 8 v2: each base pose is multiplied across 6 Fitzpatrick skin tones × 2
chiralities (right / left) for a total of ~120 hand variants. The base pose
list itself is right-handed; left chirality is generated by mirroring the
right-handed mesh about the YZ plane (which also flips the grasp axis sign).

Each hand carries a manifest entry that includes a ``grasp_axis_local`` field
— a unit 3-vector in the hand's local frame pointing along the line a wire
would lay across the closed fist. ``texture_mapping._place_hand_on_wire``
uses it to align the hand to a tangent on the wire skeleton.

Convention
----------
- Local frame: lowest Y at 0 (wrist plane), centred X/Z, longest XYZ extent
  scaled to 1 (consistent with :func:`load_object_library`).
- ``grasp_axis_local`` is the *closed-fingers axis* — a wire grasped by the
  hand should be parallel to this vector and pass through the palm centre.

Usage
-----
    python src/build_hand_objects.py
    python src/build_hand_objects.py --out data/objects --n-points 8000

The script always re-writes the hand_*.npz files (deterministic via a seed
per-pose). It updates ``data/objects/manifest.json`` *additively* — entries
for non-hand objects are kept verbatim; entries with category ``hand`` are
replaced.
"""

